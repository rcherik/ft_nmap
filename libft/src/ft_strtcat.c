char	*ft_strtcat(char *s1, const char *s2, const char *s3)
{
	int	i;

	i = 0;
	while (s1[i] != '\0')
		i++;
	while (*s2 != '\0')
	{
		s1[i++] = *s2;
		s2++;
	}
	while (*s3)
		s1[i++] = *s3++;
	s1[i] = '\0';
	return (s1);
}

#include "libft.h"

void	ft_debugnbr(int n)
{
	ft_putchar_fd('\"', 2);
	ft_putnbr_fd(n, 2);
	ft_putchar_fd('\"', 2);
	ft_putchar_fd('\n', 2);
}
